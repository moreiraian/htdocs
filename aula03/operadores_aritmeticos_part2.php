<?php
/*
    Operadores Aritméticos de atribuição
    +=acumula o valor de uma variável somando-a de um novo valor
    -=acumula o valor de uma variável subtraindo-a de um valor
    *=acumula o valor de uma variável multiplicando-a de um valor
    /=acumula o valor de uma variável dividindo-a de um valor
    %=acumula o valor de uma variável  somando com o resto de sua divisão por um outro valor dividindo-a de um valor
    ++ incrementador (soma +1 ao valor da variável)
    -- decrementador (subtrai -1 ao valor da variável)
  
    */
    #atribuição (multiplicar)
    //ex x *= y

    //declaração de variavel
    $x = 10;
    //forma normal
    $x = $x * 5;
    //forma simplificada (atrib mult)
    $x *= 5; 
    //impressão
    echo $x;
    echo "<br><hr><br>";

    #atribuição (divisão)
    //ex x /= y

    //declaração de variavel
    $y = 10;
    //forma normal
    $y = $y * 5;
    //forma simplificada (atrib div)
    $y /= 5; 
    //impressão
    echo $y;
    echo "<br><hr><br>";

    #atribuição (soma)
    //ex x += y

    //declaração de variavel
    $z = 10;
    //forma normal
    $z = $z + 5;
    //forma simplificada (atrib soma)
    $z += 5; 
    //impressão
    echo $z;
    echo "<br><hr><br>";

    #atribuição (subtração)
    //ex x -= y

    //declaração de variavel
    $a = 10;
    //forma normal
    $a = $a - 5;
    //forma simplificada (atrib sub)
    $a -= 5; 
    //impressão
    echo $a;
    echo "<br><hr><br>";



    